package org.wso2.carbon.consent.mgt.ui.dto;

public class PiiCategoryDTO {

    private int id;
    private String name;
    private String displayName;
    private String description;

    public PiiCategoryDTO(String name) {

        this.name = name;
    }

    public PiiCategoryDTO(String name, String displayName, String description) {

        this.name = name;
        this.displayName = displayName;
        this.description = description;
    }

    public PiiCategoryDTO(int id) {

        this.id = id;
    }

    public PiiCategoryDTO(int id, String name) {

        this.id = id;
        this.name = name;
    }

    public int getId() {

        return id;
    }

    public void setId(int id) {

        this.id = id;
    }

    public String getName() {

        return name;
    }

    public void setName(String name) {

        this.name = name;
    }

    public String getDisplayName() {

        return displayName;
    }

    public void setDisplayName(String displayName) {

        this.displayName = displayName;
    }

    public String getDescription() {

        return description;
    }

    public void setDescription(String description) {

        this.description = description;
    }
}
